// src/config/logger.js
const pino = require('pino');
const env = require('./env');

const logger = pino({
    level: (env.LOG_LEVEL || 'debug').toLowerCase(),
    transport: env.NODE_ENV === 'development'
        ? { target: 'pino-pretty', options: { colorize: true } }
        : undefined
});

module.exports = logger;
